"""tmux + pipe-pane plumbing for the long-lived REPL architecture.

Each claw-web session owns a tmux session named `claw-web-<id>` running
the user's `cl` wrapper (or the configured launch command) in a single
pane. We do NOT use `tmux attach-session` to relay output to the browser
— attaching invokes tmux's grid renderer, which paints in-place and
never propagates scroll-off content to the outer terminal's scrollback.
That's why xterm.js's scrollback stayed empty regardless of every
tmux/xterm config we tried.

Instead, we use tmux's `pipe-pane` mechanism:
  - `tmux pipe-pane -O -t name 'cat > <out_fifo>'` captures the pane's
    raw output stream (what the program inside actually wrote, before
    tmux paints it onto its grid).
  - `tmux pipe-pane -I -t name 'cat <in_fifo>'` feeds bytes from a fifo
    INTO the pane's stdin.

That lets us hand xterm.js the same stream a normal terminal would see
when running the program directly. xterm's main-screen scrollback fills
naturally — same as `claw` running in Windows Terminal.

Trade-offs vs `tmux attach-session`:
  - We get direct control over the byte stream — drag-select, copy/paste,
    wheel-scroll all behave like a normal terminal.
  - Resize is via `tmux resize-window` instead of TIOCSWINSZ on a PTY.
  - tmux's keyboard prefix (Ctrl+B) and copy-mode aren't accessible
    because there's no attach client; user navigates scrollback through
    xterm.js's own buffer instead.
  - We still get tmux's session persistence: claw stays alive across
    browser disconnects and server restarts.
"""

from __future__ import annotations

import errno
import fcntl
import os
import shlex
import shutil
import subprocess
import time
from pathlib import Path

_FIFO_DIR = Path("/tmp")


def session_name(session_id: int) -> str:
    """Stable tmux session name per claw-web session id."""
    return f"claw-web-{session_id}"


def _tmux_path() -> str:
    p = shutil.which("tmux")
    if not p:
        raise RuntimeError("tmux is not installed on PATH")
    return p


def _fifo_paths(session_id: int) -> tuple[Path, Path]:
    base = _FIFO_DIR / f"claw-web-{session_id}"
    return Path(str(base) + ".out"), Path(str(base) + ".in")


def _ensure_tmux_config() -> str:
    """Minimal tmux config used by every tmux command (`tmux -f <path>`).
    Bumps history-limit so multi-attach scenarios have a fuller buffer."""
    cfg = Path("/tmp/claw-web-tmux.conf")
    body = (
        "# Auto-generated by claw-web tmux_manager.\n"
        "set -g history-limit 50000\n"
    )
    cfg.write_text(body)
    return str(cfg)


def session_exists(session_id: int) -> bool:
    try:
        tmux = _tmux_path()
    except RuntimeError:
        return False
    result = subprocess.run(
        [tmux, "has-session", "-t", session_name(session_id)],
        capture_output=True,
    )
    return result.returncode == 0


def _ensure_fifos(session_id: int) -> tuple[Path, Path]:
    """Create both fifos for this session. Existing fifos are unlinked
    and remade so we always start from a clean state."""
    out_fifo, in_fifo = _fifo_paths(session_id)
    for f in (out_fifo, in_fifo):
        try:
            f.unlink()
        except FileNotFoundError:
            pass
        os.mkfifo(str(f), mode=0o600)
    return out_fifo, in_fifo


def create_session(
    session_id: int,
    cwd: Path,
    launch_cmd: list[str],
    *,
    cols: int = 120,
    rows: int = 40,
    extra_env: dict[str, str] | None = None,
) -> None:
    """Create a new tmux session running `launch_cmd` in `cwd`, plus
    output and input fifos wired through `tmux pipe-pane`."""
    tmux = _tmux_path()
    name = session_name(session_id)
    cfg = _ensure_tmux_config()
    out_fifo, in_fifo = _ensure_fifos(session_id)

    parts: list[str] = ["#!/usr/bin/env bash", "set -u"]
    parts.append("export TERM=xterm-256color")
    if extra_env:
        for k, v in extra_env.items():
            parts.append(f"export {shlex.quote(k)}={shlex.quote(v)}")
    parts.append(f"cd {shlex.quote(str(cwd))}")
    quoted_cmd = " ".join(shlex.quote(a) for a in launch_cmd)
    parts.append(_relaunch_loop_shell(quoted_cmd))
    shell_cmd = "\n".join(parts)

    subprocess.run(
        [
            tmux, "-f", cfg,
            "new-session", "-d", "-s", name,
            "-x", str(cols),
            "-y", str(rows),
            "bash", "-c", shell_cmd,
        ],
        check=True,
    )

    # Wait briefly for tmux to acknowledge the session.
    deadline = time.time() + 5.0
    while time.time() < deadline:
        if session_exists(session_id):
            break
        time.sleep(0.05)
    else:
        raise RuntimeError(f"tmux session {name} did not start in time")

    # NOTE: pipe-pane is established in `open_pipes` on every WS
    # connect, not here. The `cat` helpers tmux spawns exit when their
    # fifo counterpart (us) closes — so we re-establish them per-
    # connection rather than once per tmux session.


def open_pipes(session_id: int) -> tuple[int, int]:
    """(Re-)establish pipe-pane on the session's pane and return
    (out_fd_read, in_fd_write).

    pipe-pane is set up here, not at session creation, because the
    `cat` helpers tmux spawns exit when their fifo counterpart (the
    previous WS connection) closes. Re-establishing on every connect
    gives us fresh helpers.

    The caller is responsible for closing both fds when the WS closes.
    """
    tmux = _tmux_path()
    cfg = _ensure_tmux_config()
    name = session_name(session_id)
    out_fifo, in_fifo = _fifo_paths(session_id)

    # Make sure fifos exist (they should, from create_session, but be
    # defensive in case someone cleaned /tmp).
    for f in (out_fifo, in_fifo):
        if not f.exists():
            os.mkfifo(str(f), mode=0o600)

    # Per `man tmux`: "A pane may only be connected to one command at
    # a time, any existing pipe is closed before shell-command is
    # executed." Two separate `pipe-pane -I` and `pipe-pane -O` calls
    # (chained with `;` or not) tear each other down. Use a single
    # `-IO` invocation with a shell helper that bridges both.
    #
    # Order matters: per bash(1), "If job control is not active, the
    # standard input for asynchronous commands … is redirected from
    # /dev/null." `bash -c` is non-interactive with no job control, so
    # any `&`-backgrounded process gets /dev/null on stdin — which
    # means the OUTPUT cat (which needs helper-stdin = pane output)
    # MUST be foreground; the INPUT cat (which reads its in_fifo file
    # argument and ignores stdin) can be background.
    #   bg `cat in_fifo`    reads fifo → helper-stdout (= pane input)
    #   fg `cat > out_fifo` reads helper-stdin (= pane output)  → fifo
    # `wait` keeps the wrapper alive until the bg cat also dies.
    helper = (
        f"bash -c 'cat {shlex.quote(str(in_fifo))} & "
        f"cat > {shlex.quote(str(out_fifo))}; wait'"
    )
    cmd = [tmux, "-f", cfg, "pipe-pane", "-IO", "-t", name, helper]
    r = subprocess.run(cmd, capture_output=True, text=True)
    if r.returncode != 0:
        raise RuntimeError(f"pipe-pane -IO failed: {r.stderr.strip()}")

    # Tmux spawns the cat helpers asynchronously. Give them a moment to
    # actually open the fifos before we try; otherwise os.open(O_WRONLY)
    # on the input fifo would either block (no reader yet) or — with
    # O_NONBLOCK — return ENXIO.
    deadline = time.time() + 2.0
    in_fd = -1
    last_err: Exception | None = None
    while time.time() < deadline:
        try:
            in_fd = os.open(str(in_fifo), os.O_WRONLY | os.O_NONBLOCK)
            break
        except OSError as e:
            if e.errno == errno.ENXIO:
                last_err = e
                time.sleep(0.05)
                continue
            raise
    if in_fd < 0:
        raise RuntimeError(
            f"pipe-pane -I cat helper for {name} never opened the fifo "
            f"({last_err})"
        )
    # Switch input fd back to blocking mode for normal writes.
    flags = fcntl.fcntl(in_fd, fcntl.F_GETFL)
    fcntl.fcntl(in_fd, fcntl.F_SETFL, flags & ~os.O_NONBLOCK)

    # Open the output fifo O_RDWR so we hold both ends. With only
    # O_RDONLY, read() returns empty bytes whenever no writer is
    # currently connected — which is true *between* tmux re-spawning
    # its `cat > out_fifo` helper, when the pane briefly produces no
    # output, etc. We'd misread these as EOF and tear the WS down.
    # With O_RDWR, our process is itself a notional writer, so EOF
    # only occurs when WE close the fd; transient gaps register as
    # "no data right now" instead. We never write to this fd.
    out_fd = os.open(str(out_fifo), os.O_RDWR | os.O_NONBLOCK)

    return out_fd, in_fd


def resize_pane(session_id: int, cols: int, rows: int) -> None:
    """Resize the tmux pane (and its window) to match the browser's
    xterm.js dimensions. Without this, claw thinks the terminal is
    whatever cols/rows the pane was created at."""
    try:
        tmux = _tmux_path()
    except RuntimeError:
        return
    cfg = _ensure_tmux_config()
    name = session_name(session_id)
    subprocess.run(
        [tmux, "-f", cfg, "resize-window", "-t", name,
         "-x", str(cols), "-y", str(rows)],
        capture_output=True,
    )


def kill_session(session_id: int) -> None:
    try:
        tmux = _tmux_path()
    except RuntimeError:
        return
    subprocess.run(
        [tmux, "kill-session", "-t", session_name(session_id)],
        capture_output=True,
    )
    # Best-effort fifo cleanup.
    for f in _fifo_paths(session_id):
        try:
            f.unlink()
        except FileNotFoundError:
            pass


def list_sessions() -> list[str]:
    try:
        tmux = _tmux_path()
    except RuntimeError:
        return []
    result = subprocess.run(
        [tmux, "list-sessions", "-F", "#{session_name}"],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        return []
    return [
        name.strip()
        for name in result.stdout.splitlines()
        if name.strip().startswith("claw-web-")
    ]


def _relaunch_loop_shell(cmd: str) -> str:
    return (
        "while true; do\n"
        f"  {cmd}\n"
        "  rc=$?\n"
        "  echo\n"
        "  echo \"[claw exited code $rc — Enter to relaunch, q to quit]\"\n"
        "  IFS= read -r key || key=q\n"
        "  case \"$key\" in\n"
        "    q|Q) exit 0 ;;\n"
        "    *) ;;\n"
        "  esac\n"
        "done\n"
    )
